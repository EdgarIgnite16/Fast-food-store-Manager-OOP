public class Person {
    private String Ten;
    private String DiaChi;
    private String SDT;
    private String Tuoi;

    public Person() {
        this.Ten = null;
        this.DiaChi = null;
        this.SDT = null;
        this.Tuoi = null;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        this.Ten = ten;
    }

    public String getDiaChi() {
        return DiaChi;
    }

    public void setDiaChi(String diaChi) {
        this.DiaChi = diaChi;
    }

    public String getSDT() {
        return SDT;
    }

    public void setSDT(String SDT) {
        this.SDT = SDT;
    }

    public String getTuoi() {
        return Tuoi;
    }

    public void setTuoi(String tuoi) {
        this.Tuoi = tuoi;
    }
}
